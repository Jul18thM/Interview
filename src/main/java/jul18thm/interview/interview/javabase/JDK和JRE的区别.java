public class JDK和JRE的区别 {

    // JDK是功能齐全的Java SDK，拥有JRE所拥有的一切，同时还拥有编译器（javac）和工具（如javadoc和jdb）。JDK可以创建和编译程序。

    // JRE是java运行环境，是运行已编译Java程序所需要的所有内容的集合，包括JVM虚拟机，Java类库，Java命令和其他一些基础构件。JRE不可以创建程序。
}
